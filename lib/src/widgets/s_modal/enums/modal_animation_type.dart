/// Enumeration for Modal Animation Types
enum SModalAnimationType {
  /// Fade in and fade out animation.
  fade,

  /// Scale (zoom) in and out animation.
  scale,

  /// Slide from bottom animation.
  slideFromBottom,

  /// Slide from top animation.
  slideFromTop,

  /// Slide from left animation.
  slideFromLeft,

  /// Slide from right animation.
  slideFromRight,

  /// Custom animation.
  custom,
}
