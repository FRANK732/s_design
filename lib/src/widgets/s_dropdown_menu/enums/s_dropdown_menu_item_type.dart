/// Enumeration for Dropdown Menu Item Types
enum SDropdownMenuItemType {
  /// A standard menu item that can be tapped.
  normal,

  /// A menu item with a checkbox.
  checkbox,

  /// A menu item with a radio button.
  radio,

  /// A menu item that opens a submenu.
  submenu,

  /// A separator between menu items.
  separator,

  /// A non-interactive label.
  label,
}
