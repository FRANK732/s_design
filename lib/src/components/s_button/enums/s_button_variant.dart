/// Enum to define different variants of the [SButton].
enum SButtonVariant {
  /// Default filled button variant.
  defaultVariant,

  /// Destructive filled button variant (e.g., for delete actions).
  destructive,

  /// Destructive outlined button variant.
  destructiveOutline,

  /// Outlined button variant.
  outline,

  /// Secondary filled button variant.
  secondary,

  /// Ghost button variant (transparent background).
  ghost,

  /// Link button variant (text-only button).
  link,
}
